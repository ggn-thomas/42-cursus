#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i ;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

char	*ft_strdup(char *src)
{
	int		i ;
	int		lenght ;
	char	*dest ;

	lenght = ft_strlen(src);
	i = 0;
	if (src == NULL)
		return (NULL);
	dest = malloc(sizeof(char) * (lenght + 1));
	if (dest == NULL)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
